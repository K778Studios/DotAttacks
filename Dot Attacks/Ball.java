public class Ball
{
  private int xPos;
  private int yPos;
  
  
  public Ball(int x, int y)
  {
    xPos = x;
    yPos = y;
  }
  
  public void setPos(int x, int y)
  {
    xPos = x;
    yPos = y;
  }
  
  public int getX()
  {
    return xPos;
  }
  
  public int getY()
  {
    return yPos;
  }
  
}



